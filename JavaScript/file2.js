// operators:-
let x = 8;
let y = 3;

console.log(x + y);     // Addition: 11
console.log(x - y);     // Subtraction: 5
console.log(x * y);     // Multiplication: 24
console.log(x / y);     // Division: 2.666...
console.log(x % y);     // Modulus (remainder): 2
console.log(x ** y);    // Exponentiation: 512

console.log(x + 5);     // Addition with constant: 13
console.log(y * 2);     // Multiplication with constant: 6

console.log(x++);       // Post-increment: 8 (then x becomes 9)
console.log(x);         // After post-increment: 9

console.log(++x);       // Pre-increment: 10 (x becomes 10 before use)

console.log(y--);       // Post-decrement: 3 (then y becomes 2)
console.log(y);         // After post-decrement: 2

console.log(--y);       // Pre-decrement: 1 (y becomes 1 before use)
